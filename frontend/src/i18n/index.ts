const LOCALE_TRANSLATIONS: Record<SupportedLocale, Record<TranslationKey, string>> = {
  en: EN_TRANSLATIONS,
  es: { ...EN_TRANSLATIONS, ...ES_TRANSLATION_PATCH },
  fr: { ...EN_TRANSLATIONS, ...FR_TRANSLATION_PATCH },
  de: { ...EN_TRANSLATIONS, ...DE_TRANSLATION_PATCH },
};

const LANGUAGE_TO_LOCALE_MAP: Record<string, SupportedLocale> = {
  'English (EN)': 'en',
  'Spanish (ES)': 'es',
  'French (FR)': 'fr',
  'German (DE)': 'de',
};

let activeLocale: SupportedLocale = 'en';

export function setI18nLocale(locale: SupportedLocale) {
  activeLocale = locale;
}

export function getI18nLocale() {
  return activeLocale;
}

export function mapLanguageOptionToLocale(languageOption: string): SupportedLocale {
  return LANGUAGE_TO_LOCALE_MAP[languageOption] ?? 'en';
}

export function t(key: TranslationKey, params?: TranslationParams): string {
  const template = LOCALE_TRANSLATIONS[activeLocale][key] ?? EN_TRANSLATIONS[key] ?? key;

  if (!params) {
    return template;
  }

  return template.replace(/\{([^}]+)\}/g, (_match, token: string) => {
    const value = params[token];
    return value === undefined ? '' : String(value);
  });
}
